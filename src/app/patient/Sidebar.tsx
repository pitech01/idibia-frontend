interface SidebarProps {
    sidebarOpen: boolean;
    setSidebarOpen?: (open: boolean) => void;
    sidebarCollapsed?: boolean;
    setSidebarCollapsed?: (collapsed: boolean) => void;
    activeTab: string;
    setActiveTab: (tab: string) => void;
    onLogout: () => void;
}

export default function Sidebar({ sidebarOpen, setSidebarOpen, sidebarCollapsed, setSidebarCollapsed, activeTab, setActiveTab, onLogout }: SidebarProps) {
    const menuItems = [
        { id: 'dashboard', label: 'Dashboard', icon: <Icons.Grid /> },
        { id: 'appointments', label: 'Appointments', icon: <Icons.Calendar /> },
        { id: 'records', label: 'Health Records', icon: <Icons.Folder /> },
        { id: 'messages', label: 'Messaging', icon: <Icons.Chat /> },
        { id: 'payment', label: 'Payment', icon: <Icons.CreditCard /> },
        { id: 'resources', label: 'Resources', icon: <Icons.Book /> },
        { id: 'settings', label: 'Settings', icon: <Icons.Cog /> },
    ];

    return (
        <aside className={`dash-sidebar ${sidebarOpen ? 'open' : ''} ${sidebarCollapsed ? 'collapsed' : ''}`}>
            <div
                className="logo-area"
                style={{
                    display: 'flex',
                    alignItems: 'center',
                    justifyContent: sidebarCollapsed ? 'center' : 'space-between',
                    flexDirection: sidebarCollapsed ? 'column' : 'row',
                    gap: sidebarCollapsed ? '16px' : '0',
                    paddingRight: '0'
                }}
            >
                <img src="/logo.png" alt="IDIBIA Med" style={{ height: '50px', objectFit: 'contain' }} />

                <button
                    onClick={() => {
                        if (window.innerWidth >= 1024) {
                            if (setSidebarCollapsed) setSidebarCollapsed(!sidebarCollapsed);
                        } else {
                            if (setSidebarOpen) setSidebarOpen(!sidebarOpen);
                        }
                    }}
                    style={{ background: 'none', border: 'none', cursor: 'pointer', color: '#64748b', display: 'flex' }}
                >
                    <Icons.Menu />
                </button>
            </div>

            <nav style={{ flex: 1 }}>
                {menuItems.map(item => (
                    <div
                        key={item.id}
                        className={`nav-link ${activeTab === item.id ? 'active' : ''}`}
                        onClick={() => setActiveTab(item.id)}
                    >
                        {item.icon}
                        <span>{item.label}</span>
                    </div>
                ))}
            </nav>

            <div style={{ borderTop: '1px solid #f1f5f9', paddingTop: '16px' }}>

                <div className="nav-link" onClick={onLogout} style={{ color: '#ef4444' }}>
                    <Icons.Logout />
                    <span>Logout</span>
                </div>
            </div>
        </aside>
    );
}
